const chooseRecipe = function(bakeryA, bakeryB, recipes) {
  let possibilities = [];
  //check bakery A for possibilities
  for(let recipe of recipes) {
    for(let ingredient of bakeryA){
      if (recipe.ingredients.includes(ingredient)) possibilities.push((recipe));
    }
  }
  //check bakery B for result.  this could return incorrect answer if bakeryA and bakeryB have ingredients in common
  for(let recipe of possibilities) {
    for (let ingredient of bakeryB){
      if(recipe.ingredients.includes(ingredient)) return recipe;
    }
  }
}

let bakeryA = ['saffron', 'eggs', 'tomato paste', 'coconut', 'custard'];
let bakeryB = ['milk', 'butter', 'cream cheese'];
let recipes = [
    {
        name: 'Coconut Sponge Cake',
        ingredients: ['coconut', 'cake base']
    },
    {
        name: 'Persian Cheesecake',
        ingredients: ['saffron', 'cream cheese']
    },
    {
        name: 'Custard Surprise',
        ingredients: ['custard', 'ground beef']
    }
];

console.log(chooseRecipe(bakeryA, bakeryB, recipes));

